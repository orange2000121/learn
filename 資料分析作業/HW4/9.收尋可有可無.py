import re
msg=''''02-88223349, (02)-26669999, 02-29998888 ext 123,
        12345678.02 33887766 ext,12222,
        02-1234567, 02-123456789, 23-123456'''#測試用句子
pattern='\(?\d{2}\)?\-?\d{7,8}'#括號可有殼無，-可有可無，第一串數字需要為二，最後需要有7~8字
print(re.findall(pattern,msg))
